import math

number1 = 10
number2 = 20

print(number1 + number2)  # Addition
print(number1 - number2)  # Subtraction
print(number1 * number2)  # Multiplication
print(number1 / number2)  # Division
print(number1 ** 2)  # Exponentiation
print(number2 % number1)  # Modulus
print(number2 // number1)  # Floor Division
print(abs(-number1))  # Absolute value
print(round(3.14159, 2))  # Round to 2 decimal places

print(math.ceil(3.14))  # Round up
